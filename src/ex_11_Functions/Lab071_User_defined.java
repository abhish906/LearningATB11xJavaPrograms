package ex_11_Functions;

public class Lab071_User_defined {
    // main method which uses void return type does not return any value it just prints the statements and executes them
    public static void main(String[] args) {
        f1();
        f2();


    }

    // user defined functions are defined by the user
    //static methods are simple methods
    static void f1()
    {
        System.out.println("HELLO");
    }
    static void f2()
    {
        System.out.println("HOLA");
    }



}
