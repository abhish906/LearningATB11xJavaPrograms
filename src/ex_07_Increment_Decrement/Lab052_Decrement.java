package ex_07_Increment_Decrement;

public class Lab052_Decrement {
    public static void main(String[] args) {

        // pre increment
        int a=10;
        int b=a--;
        System.out.println(a);
        System.out.println(b);

        //Post increment
        int c=10;
        int d=--c;
        System.out.println(c);
        System.out.println(d);
    }
}
